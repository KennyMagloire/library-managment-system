package com.library;

import java.time.LocalDate;

public class Report {

    private String reportId;
    private ReportType reportType;
    private LocalDate generatedDate;
    private String content;
    private SystemUser generatedBy;

    public Report(String reportId, ReportType reportType, SystemUser generatedBy) {
        this.reportId = reportId;
        this.reportType = reportType;
        this.generatedBy = generatedBy;
        this.generatedDate = LocalDate.now();
        this.content = "";
    }

    // Getters
    public String getReportId() { return reportId; }
    public ReportType getReportType() { return reportType; }
    public LocalDate getGeneratedDate() { return generatedDate; }
    public String getContent() { return content; }
    public SystemUser getGeneratedBy() { return generatedBy; }


    public void generate() {
        this.content = "Report Type: " + reportType
                + "\nGenerated By: " + generatedBy.getUsername()
                + "\nDate: " + generatedDate;
    }

    public void export(String format) {
        System.out.println("Exporting report as " + format + "...");
        System.out.println(content);
    }

}